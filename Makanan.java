    private void tabelMakananMouseClicked(java.awt.event.MouseEvent evt) {                                          
        // TODO add your handling code here:
        int baris = tabelMakanan.rowAtPoint(evt.getPoint());
        String id_makanan = tabelMakanan.getValueAt(baris, 1).toString();
        txtID.setText(id_makanan);
        
        String nama_makanan = tabelMakanan.getValueAt(baris, 2).toString();
        txtMakanan.setText(nama_makanan);
        
        String harga = tabelMakanan.getValueAt(baris, 1).toString();
        txtHarga.setText(harga);
    }                                         

