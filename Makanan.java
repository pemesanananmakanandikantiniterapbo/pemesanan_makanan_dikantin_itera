    private void tbEditActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        try {
            String sql = "UPDATE Makanan SET id_makanan ='"+txtID.getText()+"',Nama Makanan ='"+txtMakanan.getText()+"',Harga ='"+txtHarga.getText()+"' WHERE id_makanan = '"+txtID.getText()+"'";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil...");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        tampilkan_data();
        kosongkan_form();
        
    }                                      

