    private void tampilkan_data() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID Makanan");
        model.addColumn("Nama Makanan");
        model.addColumn("Harga Makanan");
        
        try {
            int no = 1;
            String sql = "SELECT * FROM Makanan";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()) {
                model.addRow (new Object[]{no++,res.getString(1),res.getString(2),res.getString(3)});
            }
            tabelMakanan.setModel(model);
            
        } catch (SQLException e) {
            System.out.println("Error :" + e.getMessage());
        }
    }
